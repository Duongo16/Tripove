    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        AccountDAO ad = new AccountDAO();
        SeatDAO sd = new SeatDAO();
        HttpSession session = request.getSession();

        Integer idd = (Integer) session.getAttribute("id");
        int i = (idd != null) ? idd : -1;

        Account acc = ad.getAccountById(i);
        request.setAttribute("account", acc);
        
        String pickUp = request.getParameter("pickUp");
        String selectedSeats = request.getParameter("selectedSeatsHidden");
        String totalPriceStr = request.getParameter("totalPriceHidden");
        String currentRouteDetailIdStr = request.getParameter("currentRouteDetailId");
        
        try {
            int totalPrice = Integer.parseInt(totalPriceStr);
            int currentRouteDetail = Integer.parseInt(currentRouteDetailIdStr);
            request.setAttribute("totalPrice", totalPrice);
            for(String selected : selectedSeats.split(", ")){
                sd.buyTicket(selected, currentRouteDetail, pickUp, i);
            }
            request.getRequestDispatcher("successfulBuying.jsp").forward(request, response);
        } catch (Exception e) {
        }
    }

